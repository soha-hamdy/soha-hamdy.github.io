var birthday = moment([2001, 9, 9]);
var today = new Date();
var curr_day = moment([today.getFullYear(), today.getMonth() + 1, today.getDate()]);
var duration = moment.duration(curr_day.diff(birthday));
var years = duration.years();
var months = duration.months();
var days = duration.days();
